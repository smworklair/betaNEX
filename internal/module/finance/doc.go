// Пакет finance — модуль «Финансы»: леджер двойной записи (ADR-021).
//
// Ядро модели — классическая двойная запись на свободном открытом ядре
// (PostgreSQL): план счетов и сбалансированные проводки. Этого примитива
// достаточно для всего раздела «Финансы» КИС: платежи студентов — проводка
// «касса/банк → доход», начисление платы — «дебиторка студента → доход»,
// задолженность — сальдо лицевого счёта студента, бюджет — счета расходов.
//
// Правила модуля (см. docs/how-to-write-a-module.md):
//   - деньги — int64 в минорных единицах валюты (копейках), никаких float;
//   - каждая проводка сбалансирована: сумма дебетов равна сумме кредитов;
//   - проводки не изменяются и не удаляются — исправление делается
//     сторнирующей проводкой (append-only, как и аудит);
//   - все изменения — через шину команд; чтение — через Repository.
//
// Хранилище: интерфейс Repository. Сейчас работает MemoryRepository
// (данные в памяти процесса — для разработки и тестов); Postgres-реализация
// на sqlc придёт на вехе M2, черновики: migrations/00002_finance.sql и
// internal/platform/postgres/queries/finance.sql.
