// Пакет module — корень доменных модулей NEX.
//
// Модуль — самодостаточный срез предметной области (например, module/college:
// студенты, группы, преподаватели). Каждый модуль содержит свои команды,
// события, SQL-запросы (sqlc) и HTTP-маршруты и регистрирует их в ядре
// при старте приложения.
//
// Правила:
//   - модуль зависит только от ядра (internal/kernel) и платформы
//     (internal/platform), но не от других модулей;
//   - интеграция модулей между собой — через события, а не прямые вызовы;
//   - изменения данных — только через шину команд.
//
// Первый модуль появляется на вехе M6 (docs/roadmap.md); шаблон структуры
// модуля будет описан там же в docs/how-to-write-a-module.md.
