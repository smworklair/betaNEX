// Package command — шина команд: единственный путь изменения данных в NEX.
//
// Жизненный цикл команды (веха M2):
//
//	HTTP-хендлер → Bus.Dispatch(ctx, cmd)
//	  1. валидация      cmd.Validate()
//	  2. авторизация    authz: актор имеет право cmd.Permission() в tenant'е
//	  3. транзакция     исполнение хендлера команды в одной tx
//	  4. события        доменные события пишутся в той же tx
//	  5. аудит          запись «кто, что, когда, результат» — в той же tx
//
// Это набросок контракта, не финальный API: сигнатуры уточнятся,
// когда появится слой postgres (веха M1) и события (пакет event).
package command

import "context"

// Command — намерение изменить состояние системы.
// Команда несёт данные и знает, как себя валидировать
// и какое право требуется для её исполнения.
type Command interface {
	// Name — стабильное имя команды для аудита, например "college.student.enroll".
	Name() string
	// Permission — право, требуемое для исполнения (проверяет authz).
	Permission() string
	// Validate проверяет инварианты входных данных до любых обращений к БД.
	Validate() error
}

// Handler исполняет команду конкретного типа внутри транзакции,
// открытой шиной. Регистрируется модулем при старте приложения.
type Handler[C Command] interface {
	Handle(ctx context.Context, cmd C) error
}

// Bus принимает команды и проводит их по полному циклу:
// валидация → авторизация → транзакция → события → аудит.
type Bus interface {
	Dispatch(ctx context.Context, cmd Command) error
}
