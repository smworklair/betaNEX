// Пакет event — доменные события: факты, которые уже произошли.
//
// События порождаются хендлерами команд и фиксируются в той же
// транзакции, что и изменение данных (outbox-подход). Подписчики —
// в том числе фоновые задачи (River) — обрабатывают их после коммита,
// поэтому событие не может «убежать» от несостоявшегося изменения.
//
// Набросок контракта (веха M2); сериализация и доставка уточнятся
// при интеграции River.
package event

import "time"

// Event — неизменяемый факт предметной области.
type Event interface {
	// Name — стабильное имя события, например "college.student.enrolled".
	Name() string
	// OccurredAt — момент, когда факт произошёл.
	OccurredAt() time.Time
}
