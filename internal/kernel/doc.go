// Пакет kernel — доменно-независимое ядро NEX.
//
// Ядро отвечает за четыре вещи и только за них:
//
//   - identity — кто актор (пользователи, сессии, учётные данные);
//   - tenancy  — в каком tenant'е он действует (изоляция данных организаций);
//   - authz    — что ему разрешено (роли и права, скоупы per-tenant);
//   - command/event/audit — единый путь изменений: команда проходит
//     валидацию и авторизацию, исполняется в транзакции, порождает события,
//     каждое действие фиксируется в журнале аудита.
//
// Правило зависимостей: ядро не знает ничего о модулях и приложениях.
// Модули (internal/module/...) зависят от ядра, но не друг от друга.
// AI — будущий актор, использующий те же авторизованные пути, а не часть ядра.
//
// Наполнение пакетов — вехи M2–M4 роадмапа (docs/roadmap.md).
package kernel
