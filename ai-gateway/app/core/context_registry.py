# Fallback для разделов, которых нет в SECTION_PROMPTS (новый раздел во
# фронтенде, ещё не описанный здесь, или произвольный `page`).
GENERIC_SECTION_PROMPT = "Ты — помощник NEX на разделе «{title}». Отвечай по существу вопроса и видимых данных экрана."


def _section_prompt(context: PageContext) -> str:
    title = context.title or context.page
    return SECTION_PROMPTS.get(context.page, GENERIC_SECTION_PROMPT.format(title=title))


def build_context_block(context: PageContext | None) -> str | None:
    """
    Собирает фрагмент системного промпта из контекста страницы: роль
    раздела + факты/состояние экрана. Возвращает None, если context не
    передан — тогда ai_service.py использует только DEFAULT_SYSTEM_PROMPT.
    """
    if context is None:
        return None

    parts = [_section_prompt(context)]
    if context.facts:
        parts.append("Текущие данные экрана: " + "; ".join(context.facts) + ".")
    if context.state:
        parts.append(f"Состояние экрана: {context.state}")
    return " ".join(parts)
